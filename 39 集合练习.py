my_fruit = {'mango', 'strawberry', 'pear'}
his_fruit = {'banana', 'apple', 'pear'}
union_result = my_fruit.union(his_fruit)
intersection_result = my_fruit.intersection(his_fruit)
difference_result = my_fruit.difference(his_fruit)
print(union_result)
print(intersection_result)
print(difference_result)