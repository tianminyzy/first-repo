# 1. Python 派系：靠 “缩进” 说话
# 在 Python 中，while 后面所有缩进空格一致的代码行，都被视为属于这个循环。
# 一旦某一行代码不再缩进（回到了左边竖线），它就不再属于 while 了。
n = 0 # while前 变量要初始化(给它赋值)
while n < 3:
    print("这是第一行")  # 属于循环
    print("这是第二行")  # 属于循环
    n += 1              # 属于循环
print("我不缩进，所以我只在循环彻底结束后运行一次")
# 2. C / Java / JavaScript 派系：靠“大括号”说话
# 在这些语言中，while 默认确实只管辖紧随其后的一条语句。
# 但程序员几乎都会使用 { }（大括号）来强制让它管辖多行。
'''while (n < 3) {
    printf("第一行"); // 属于循环
    printf("第二行"); // 属于循环
    n++;             // 属于循环
}'''
# 无论你学的是哪种语言，为了代码的可读性和避免 Bug，建议遵循以下原则：
# Python： 严格对齐缩进。
# 其他语言： 哪怕循环里只有一行代码，也请务必加上 { }。