grocery = ['🥚 Eggs',
'🥑 Avocados',
'🍪 Cookies',
'🌶 Hot Pepper Jam',
'🫐 Blueberries',
'🥦 Broccoli']
# 用换行符连接列表中的每一项
print('\n'.join(grocery))
'''
1. '\n' 是什么？

在编程里，\n 是一个转义字符，代表**“换行”**。

    当你打印 '\n' 时，就像在键盘上敲了一下 Enter 键。

    我们把它放在最前面，意思是：“我要用‘换行’作为胶水”。

2. .join() 是做什么的？

你可以把 .join() 想象成一个**“粘合剂”**。它的逻辑是：

    "胶水".join(列表)

它会把列表里的每一个元素拿出来，然后在它们中间粘上你指定的“胶水”。'''